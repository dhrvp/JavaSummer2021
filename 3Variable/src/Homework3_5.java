
public class Homework3_5 {

	public static void main(String[] args) {
		// Increment the variable (+)
		int BigNumber = 0;
		BigNumber++;
		System.out.println("The value after positive increment is " + BigNumber);

		// Increment the variable (-)
		int BigNumber2 = 32;
		BigNumber2--;
		System.out.println("The value after negative increment is " + BigNumber2);

		// Invert boolean value
		boolean LargeNumber = false;

		boolean SmallNumber = !LargeNumber;
		System.out.println("The invert of the boolean large number value is " + SmallNumber);

	}
}
